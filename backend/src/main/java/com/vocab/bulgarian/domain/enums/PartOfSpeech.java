package com.vocab.bulgarian.domain.enums;

/**
 * Bulgarian grammatical parts of speech.
 * Includes INTERROGATIVE for question words and PARTICLE for Bulgarian particles.
 */
public enum PartOfSpeech {
    NOUN,
    VERB,
    ADJECTIVE,
    ADVERB,
    PRONOUN,
    PREPOSITION,
    CONJUNCTION,
    NUMERAL,
    INTERJECTION,
    PARTICLE,
    INTERROGATIVE
}
